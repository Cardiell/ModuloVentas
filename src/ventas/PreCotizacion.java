    @Override
    public void run() {
        try {
            ImageIcon lblDisp= new ImageIcon(mat.getImagen());
            Icon icono = new ImageIcon(lblDisp.getImage().getScaledInstance(lblImagen.getWidth()-3,lblImagen.getHeight()-3,Image.SCALE_DEFAULT));
            Thread.sleep(100);
            lblPrecio.setText(String.valueOf(mat.getPrecio()));
            lblNombre.setText(mat.getNombreMaterial());
            lblImagen.setIcon(icono);
            CotizarMaterial tmp = new CotizarMaterial(idCotizar,idMat,cantidad);
            if(bandera){
                    lblAlerta.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/alerta.png")));
                    lblmsg.setText("Este producto ya estaba en tu Carrito. Si no quieres "+cantidad+" de este producto,ve a Cotizar");
                    cotizacion.UpdateCotizarMaterial(tmp);
            }
            else{   
            cotizacion.AgregarCotizarMaterial(tmp);
            }
            Thread.sleep(1500);
            this.dispose();
        } catch (InterruptedException ex) {
            Logger.getLogger(PreCotizacion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
