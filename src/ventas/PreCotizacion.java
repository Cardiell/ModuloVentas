    public PreCotizacion(java.awt.Frame parent, boolean modal,Object idMat) {
        super(parent, modal);
        this.idMat= (int) idMat;
        Thread hilo = new Thread(this);
        material =new MaterialLog();
        cotizacion = new CotizarMaterialLog();
        bandera=false;
        cantidad=0;
        mat=material.BuscarMaterialesId(this.idMat);
        CotizarMaterial cot=cotizacion.BuscarCotizarMaterial(idCotizar,mat.getIdMaterial());
        try {
            if(cotizacion.OkCotizarMaterial(idCotizar,mat.getIdMaterial())){
                cantidad= cot.getCantidad();
                bandera =true;
            }
            cantidad++;
        } catch (SQLException ex) {
            Logger.getLogger(PreCotizacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        initComponents();
        setLocationRelativeTo(null);
        this.setOpacity((float) 0.95);
        if(cantidad <= mat.getExistencia()){
        jLabel1.setFont(new java.awt.Font("Arial", 1, 21)); // NOI18N             //PRODUCTO AGREGADO CORRECTAMENTE
        jLabel1.setForeground(new java.awt.Color(0, 138, 0));
        jLabel1.setText("Producto agregado Correctamente");
        jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/palomita.png"))); // NOI18N   IMAGEN PALOMITA
        }
        else{
        jLabel1.setFont(new java.awt.Font("Arial", 1, 21)); // NOI18N             //PRODUCTO AGREGADO CORRECTAMENTE
        jLabel1.setForeground(new java.awt.Color(185, 0, 0));
        jLabel1.setText("Error al ingresar el producto");
        jLabel2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/imagenes/x.png"))); // NOI18N   IMAGEN PALOMITA
        cantidad--;
        lblmsg.setVisible(false);
        lblAlerta.setVisible(false);
        }
        hilo.start();


    }

